let firendNames : string []=["zara", "saba","fara","sara",]
console.log (firendNames)  // output [ 'zara', 'saba', 'fara', 'sara' ]

//let remove last elements from array by using method pop()
firendNames.pop()
console.log (firendNames);  //output [ 'zara', 'saba', 'fara' ]  removed

//let add new array in the last element
firendNames.push("sabahat");
console.log (firendNames) //output [ 'zara', 'saba', 'fara', 'sabahat' ] added
